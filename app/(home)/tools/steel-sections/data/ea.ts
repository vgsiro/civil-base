// Equal Angles (EA) — Advance® UKA
// BS EN 1993-1-1:2005 + BS EN 10056-1:1999 / SCI P363 Blue Book
// Axis convention: y-y & z-z = legs; u-u = major principal; v-v = minor principal
// cy = cz = distance from centroid to back of leg (equal angles: cy = cz)
// r1 = root radius, r2 = toe radius (stored as r = r1, tw = t for leg thickness)
// Blue Book column mapping:
//   h = b = leg size (mm); tw = t = leg thickness (mm); r = r1 (root radius)
//   Ix = Iy = I_yy = I_zz; Wx = Wy = W_el,yy = W_el,zz
//   Iu = I_uu (major principal); Iv = I_vv (minor principal)
//   ix = iy; iu, iv; Wu, Wv (equal angles: Wu used for both)
//   tanA = 1.00 for equal angles (axes at 45°)
import type { SectionRow } from '../_shared/types'

function ea(
  des: string, mass: number,
  h: number, t: number, r1: number, r2: number,
  cy: number,
  Iyy: number, iyz: number, Wyy: number,
  Iuu: number, iuu: number, Ivv: number, ivv: number, Wuu: number,
  IT: number, phiMin: number, phiMax: number, vs: number,
  A: number,
): SectionRow {
  return {
    designation: des, mass,
    h, b: h, tw: t, tf: t, r: r1,
    A,
    Ix: Iyy, Iy: Iyy,
    Wx: Wyy, Wy: Wyy,
    Zx: 0,   Zy: 0,
    ix: iyz, iy: iyz,
    J: IT, Cw: 0,
    Iu: Iuu, iu: iuu,
    Iv: Ivv, iv: ivv,
    Wu: Wuu, Wv: Wuu,
    tanA: 1.00,
    phiMin, phiMax,
    vs,
    cy, cz: cy,
  }
}

