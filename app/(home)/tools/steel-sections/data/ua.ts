// BS EN 1993-1-1:2005 + BS EN 10056-1:1999 / SCI P363 Blue Book
// h = long leg, b = short leg, tw = t = leg thickness
// cy = dist from centroid to back of long leg face (y), cz = dist to back of short leg face (z)
// Axis mapping: y-y axis → Ix, z-z axis → Iy; u-u & v-v = principal axes
// tanA = tan of angle between v-v axis and z-z axis
// vs = monosymmetry index
import type { SectionRow } from '../_shared/types'

function ua(
  des: string, mass: number,
  h: number, b: number, t: number, r1: number,
  cy: number, cz: number,
  Iyy: number, Izz: number, Iuu: number, Ivv: number,
  iyy: number, izz: number, iuu: number, ivv: number,
  Wyy: number, Wzz: number,
  tanA: number,
  IT: number, phiMin: number, phiMax: number, vs: number,
  A: number,
): SectionRow {
  return {
    designation: des, mass,
    h, b, tw: t, tf: t, r: r1,
    A,
    Ix: Iyy, Iy: Izz,
    Wx: Wyy, Wy: Wzz,
    Zx: 0,   Zy: 0,
    ix: iyy, iy: izz,
    J: IT, Cw: 0,
    Iu: Iuu, iu: iuu,
    Iv: Ivv, iv: ivv,
    tanA, phiMin, phiMax, vs,
    cy, cz,
  }
}

