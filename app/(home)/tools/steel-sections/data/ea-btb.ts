// Equal Angles Back to Back — Advance® UKA Equal Angles Back to Back
// BS EN 1993-1-1:2005 + BS EN 10056-1:1999 / SCI P363 Blue Book
// Two equal angles placed back to back (legs outward, connected at root).
// Iy (→ Ix) = combined I about y-y axis (= 2 × single I_yy)
// Iy_s (→ Iy) = combined I about z-z axis at given spacing s = 0
// iz values vary with space between backs, s = 0, 8, 10, 12, 15 mm
// We store s = 0 value as base; ix from Iy/A; iy = iz at s=0
import type { SectionRow } from '../_shared/types'

function eaBtb(
  des: string, mass: number,
  ry: number, // root radius (single angle)
  A: number,
  Iy: number,   // cm⁴ — combined I_yy (major)
  Iz: number,   // cm⁴ — combined I_zz at s=0
  iy: number,   // cm  — i_yy
  Wady: number, // cm³ — elastic modulus y-y
  iz0: number,  iz8: number, iz10: number, iz12: number, iz15: number, // cm
): SectionRow {
  return {
    designation: des, mass,
    h: 0, b: 0, tw: 0, tf: 0, r: ry,
    A,
    Ix: Iy, Iy: Iz,
    Wx: Wady, Wy: 0,
    Zx: 0, Zy: 0,
    ix: iy, iy: iz0,
    J: 0, Cw: 0,
    s: 0,
  }
}

