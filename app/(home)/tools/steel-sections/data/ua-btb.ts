// Unequal Angles Back to Back — Advance® UKA Unequal Angles Back to Back
// BS EN 1993-1-1:2005 + BS EN 10056-1:1999 / SCI P363 Blue Book
// Two unequal angles placed back to back (long legs outward).
// Iy (→ Ix) = combined I about y-y (= 2 × single I_yy, major axis)
// Iy_f (→ Iy) = combined I about z-z at s=0
// iz varies with space s = 0, 8, 10, 12, 15 mm — stored at s=0 as base iy
import type { SectionRow } from '../_shared/types'

function uaBtb(
  des: string, mass: number,
  ry: number,
  A: number,
  Iyy: number,   // cm⁴ — 2× I_yy (combined major)
  Izz_s0: number, // cm⁴ — combined I_zz at s=0
  iyy: number,   // cm  — i_yy
  Wady: number,  // cm³ — elastic modulus y-y
  iz0: number, iz8: number, iz10: number, iz12: number, iz15: number,
): SectionRow {
  return {
    designation: des, mass,
    h: 0, b: 0, tw: 0, tf: 0, r: ry,
    A,
    Ix: Iyy, Iy: Izz_s0,
    Wx: Wady, Wy: 0,
    Zx: 0, Zy: 0,
    ix: iyy, iy: iz0,
    J: 0, Cw: 0,
    s: 0,
  }
}

